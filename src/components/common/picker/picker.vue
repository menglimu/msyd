<!-- 
  slots

  绑定到 slots 属性的数组由对象组成，每个对象都对应一个 slot，它们有如下键名
  key 描述
  divider 对应 slot 是否为分隔符
  content 分隔符 slot 的显示文本
  values  对应 slot 的备选值数组。若为对象数组，则需在 mt-picker 标签上设置 value-key 属性来指定显示的字段名
  defaultIndex  对应 slot 初始选中值，需传入其在 values 数组中的序号，默认为 0
  textAlign 对应 slot 的对齐方式
  flex  对应 slot CSS 的 flex 值
  className 对应 slot 的类名
  API

  参数  说明  类型  可选值 默认值
  slots slot 对象数组 Array   []
  valueKey  当 values 为对象数组时，作为文本显示在 Picker 中的对应字段的字段名 String    ''
  showToolbar 是否在组件顶部显示一个 toolbar，内容自定义 Boolean   false
  visibleItemCount  slot 中可见备选值的个数  Number    5
  itemHeight  每个 slot 的高度 Number    36
  Slot

  name  描述
- 当 showToolbar 为 true 时，toolbar 中的内容
  http://mint-ui.github.io/docs/#/zh-cn2/picker
 -->
<template>
  <div class="picker" :class="{ 'picker-3d': rotateEffect }">
    <div class="picker-toolbar" v-if="showToolbar"><slot></slot></div>
    <div class="picker-items">
      <picker-slot v-for="slot in slots" :valueKey="valueKey" :values="slot.values || []" :text-align="slot.textAlign || 'center'" :visible-item-count="visibleItemCount" :class-name="slot.className" :flex="slot.flex" v-model="values[slot.valueIndex]" :rotate-effect="rotateEffect" :divider="slot.divider" :content="slot.content" :itemHeight="itemHeight" :default-index="slot.defaultIndex"></picker-slot>
      <div class="picker-center-highlight" :style="{ height: itemHeight + 'px', marginTop: -itemHeight / 2 + 'px' }"></div>
    </div>
  </div>
</template>

